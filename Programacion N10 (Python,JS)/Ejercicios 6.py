print("Ingrese la altura y la base del rectangulo:  ")
altura=float(input("Ingrese el numero: "));
base=float(input("Ingrese la base: "));
print("Calculando la superficie y el perimetro.....");
superficie=(altura*base);
perimetro=2*(base+altura);
print("La superficie es:  ",superficie);
print("El perimetro es:  ",perimetro);
print("Los numeros ingresados fueron: ",altura," , ",base)